int main()
{
	//test();
	WorkerManager wm;
	int choice = 0;

	while (true)
	{
		// 展示菜单
		wm.showMenu();
		cout << "请输入您的选择：" << endl;
		while (!(cin >> choice))
		{
			cin.clear();
			while (cin.get() != '\n')
			{
				continue;
			}
			cout << "请重新输入选择：" << endl;
		}

		switch (choice)
		{
		case 0: // 退出系统
			wm.exitSystem();
			break;
		case 1: // 添加职工
			wm.addEmp();
			break;
		case 2: // 显示职工
			wm.showEmp();
			break;
		case 3: // 删除职工
			wm.delEmp();
			break;
		case 4: // 修改职工
			wm.modEmp();
			break;
		case 5: // 查找职工
			wm.findEmp();
			break;
		case 6: // 排序职工
			wm.sortEmp();
			break;
		case 7: // 清空文件
			wm.cleanFile();
			break;
		default:
			system("cls");
			break;
		}
	}

	system("pause");

	return 0;
}